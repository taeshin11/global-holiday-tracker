  currentLang: 'EN',

  init() {
    // ?lang=xx URL parameter takes highest priority
    const urlParam = new URLSearchParams(window.location.search).get('lang');
    if (urlParam) {
      const urlLang = urlParam.toUpperCase();
      if (this.translations[urlLang]) {
        this.currentLang = urlLang;
        localStorage.setItem('ght_language', urlLang);
        this.updatePageLanguage();
        return;
      }
    }
    const saved = localStorage.getItem('ght_language');
    if (saved && this.translations[saved]) {
      this.currentLang = saved;
    } else {
      const browserLang = (navigator.language || 'en').substring(0, 2).toUpperCase();
      const mapping = { KO: 'KO', JA: 'JA', ZH: 'ZH', ES: 'ES', DE: 'DE', FR: 'FR', PT: 'PT' };
      this.currentLang = mapping[browserLang] || 'EN';
    }
    this.updatePageLanguage();
  },

  t(key) {
    return (this.translations[this.currentLang] && this.translations[this.currentLang][key])
      || (this.translations['EN'] && this.translations['EN'][key])
      || key;
  },

  setLanguage(lang) {
    if (this.translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('ght_language', lang);
      this.updatePageLanguage();
      document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    }
  },

  updatePageLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = this.t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      el.title = this.t(key);
    });
    document.documentElement.lang = this.currentLang.toLowerCase();
  },

  getLocale() {
    const map = { EN: 'en-US', KO: 'ko-KR', JA: 'ja-JP', ZH: 'zh-CN', ES: 'es-ES', DE: 'de-DE', FR: 'fr-FR', PT: 'pt-BR' };
    return map[this.currentLang] || 'en-US';
  },

  formatDate(dateStr) {
    const date = new Date(dateStr + 'T00:00:00');
    return new Intl.DateTimeFormat(this.getLocale(), { year: 'numeric', month: 'long', day: 'numeric' }).format(date);
  },

